DROP TABLE IF EXISTS structures CASCADE;
DROP TABLE IF EXISTS consultants CASCADE;

CREATE TABLE IF NOT EXISTS structures (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  division VARCHAR(250) NOT NULL,
  direction VARCHAR(250) NOT NULL,
  office VARCHAR(250) NOT NULL,
  sub_division VARCHAR(250),
  CONSTRAINT pk_structure PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS consultants (
   id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   user_full_name VARCHAR(250) NOT NULL,
   number_Of_Task FLOAT NOT NULL,
   structure_id BIGINT NOT NULL REFERENCES structures(id) ON DELETE CASCADE,
   CONSTRAINT pk_consultants PRIMARY KEY (id)
);